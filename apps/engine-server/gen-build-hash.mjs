// Generates src/build-hash.ts before esbuild bundles the server.
// Run via the "gen-hash" Nx target; never edit the output file by hand.
import { execSync } from 'child_process';
import { writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const dir = dirname(fileURLToPath(import.meta.url));

let hash = 'dev';
try {
  hash = execSync('git rev-parse --short HEAD', { stdio: ['ignore', 'pipe', 'ignore'] })
    .toString()
    .trim();
} catch {
  // git not available
}

writeFileSync(
  join(dir, 'src/build-hash.ts'),
  `// Auto-generated by gen-build-hash.mjs — do not edit\nexport const BUILD_HASH = ${JSON.stringify(hash)};\n`,
);

console.log(`[gen-hash] BUILD_HASH = ${hash}`);
